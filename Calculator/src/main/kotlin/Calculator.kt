class Calculator {

    fun addition(num1: Int, num2: Int) : Int {
        return  num1 + num2
    }

    fun subtraction(num1: Int, num2: Double): Double {
        return num1 - num2
    }

    fun multiplication(num1: Int, num2: Long, num3: Short): Long {
        return num1 * num2 * num3
    }

    fun division(num1: Int, num2: Float): Float {
        return num1 / num2
    }
}